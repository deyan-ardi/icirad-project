                            <h5 class="mt-3"><ins> Geographic & Climate</ins></h5>
                            <p class="text-justify">Geographically, Bali lies between the islands of Java and Lombok. Bali is small, stretching approximately 140 km from east to west 80 km from north to south. The tallest of a string of volcanic mountains that run from the east to the west, is Gunung Agung, which last erupted in 1963. Lying just 8 south of the equator, Bali boasts a tropical climate with just two seasons (wet and dry) a year and an average annual temperature of around 23C to 33C. High humidity can be expected during the Wet Season between the months of October - April. The Dry Season between the months of May - September have also the lowest humidity.</p>
                            <p class="text-justify">
                                The Wet Season brings daily rain and quiet overcast days with the most rain recorded between December - February. Occasionally rainfall can also be expected during the dry season but usually at night or very early morning. June - August there is usually a very refreshing cool breeze all day long. The central mountain area is typically cooler than the lower coastal areas mainly especially at night.</p>


                            <h5 class="mt-3"><ins> People & Religion</ins></h5>
                            <p class="text-justify">With 3,409,845 million people (Statistic 2008), Bali is a very densely populated island. The population is almost all Indonesian, with the usual small Chinese contingent in the big towns, a sprinkling of Indian merchants, plus a number of more or less permanent visitors amongst the Westerners in Bali.</p>
                            <p class="text-justify">
                                Balinese people have been Hindus for eight hundred years, since the remnants of the Majapahit empire were forced from Java by the spread of Islam. They follow a branch of Hinduism that owes a lot to that of India, but is quite different. The most obvious discrepancy is that the Balinese eat cows, but there are numerous others.</p>

                            <h5 class="mt-3"><ins> Culture</ins></h5>
                            <p class="text-justify">Unlike any other island in largely Muslim Indonesia, Bali is a pocket of Hindu religion and culture. Every aspect of Balinese life is suffused with religion, but the most visible signs are the tiny offerings (canang sari) found in every Balinese house, work place, restaurant, souvenir stall and airport check-in desk. These leaf trays are made daily and can contain an enormous range of offering items: flowers, glutinous rice, cookies, salt, and even cigarettes and coffee! They are set out with burning incense sticks and sprinkled with holy water no less than three times a day, before every meal. Don't worry if you step on one, as they are placed on the ground for this very purpose and will be swept away anyway.</p>
