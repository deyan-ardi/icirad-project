                            <h2>About Universitas Pendidikan Ganesha (Undiksha)</h2>
                            <p class="text-justify">Universitas Pendidikan Ganesha (UNDIKSHA) is a state-run higher education institution which already scored human resources both in the education and non-education sector. In the field of education Undiksha is the biggest scorer of human resources in Bali. Up to now Undiksha already produced more than thirty thousand graduates, most of them being education personnel. The history of Undiksha started with the B-1 course to prepare teachers for the Indonesian Language in 1955 and business teachers on high school level in 1957. In the year 1962 both mentioned courses were joined to become the Faculty of Teachership and Education Science of the Airlangga University. In the same year FKIP became a part of the Udayana University and in 1963 became the Singaraja branch of IKIP Malang. In the year 1968 FKIP was made into the two faculties Teachership and Science Education, then returned as a part of the Udayana University. In the year 1981 FKG and FIP were merged to become FKIP University Udayana. In the year 1993 FKIP split from UNUD to become STKIP Singaraja and in the year 2001 became IKIP (Negeri) Singaraja. After the long process to go through both the mentioned courses finally became Undiksha after IKIP Singaraja´s status was changed into becoming “Universitas Pendidikan Ganesha” (Undiksha) by the presidential order number 11/2006, date 11th May 2006. </p>
